
function jumlahAngka(a,b){
	
	var hasiljumlah = parseInt(a)+parseInt(b);
	alert(hasiljumlah);
	return hasiljumlah;
	
}

function kurangAngka(a,b){
	
	var hasiljumlah = parseInt(a)-parseInt(b);
	alert(hasiljumlah);
	return hasiljumlah;
	
}

function kaliAngka(a,b){
	
	var hasiljumlah = parseInt(a)*parseInt(b);
	alert(hasiljumlah);
	return hasiljumlah;
	
}

function bagiAngka(a,b){
	
	var hasiljumlah = parseInt(a)/parseInt(b);
	alert(hasiljumlah);
	return hasiljumlah;
	
}